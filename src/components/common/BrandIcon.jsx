/**
 * BrandIcon 컴포넌트
 * @param {string}  platform  - 'twitter' | 'instagram' | 'youtube' | 'pixiv' | 'bluesky' | 'github'
 * @param {number}  size      - px 크기 (기본 16)
 * @param {string}  color     - 색상 오버라이드 (기본: 플랫폼 브랜드 색상)
 * @param {string}  className
 */
export default function BrandIcon({ platform, size = 16, color, className }) {
  const icon = ICONS[platform]
  if (!icon) return null

  return (
    <svg
      width={size}
      height={size}
      viewBox="0 0 24 24"
      fill={color ?? icon.color}
      role="img"
      aria-label={icon.label}
      className={className}
    >
      <title>{icon.label}</title>
      <path d={icon.path} />
    </svg>
  )
}

/** 플랫폼별 브랜드 색상 반환 */
export function getBrandColor(platform) {
  return ICONS[platform]?.color ?? 'currentColor'
}

/** 플랫폼 라벨 반환 */
export function getBrandLabel(platform) {
  return ICONS[platform]?.label ?? platform
}

